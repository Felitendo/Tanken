/** Resolves the catalogue from the persisted setting ("auto"/"de"/"en") + the device language. */
fun stringsFor(languageSetting: String, platformCode: String): Strings = when (languageSetting) {
    "de" -> StringsDe
    "en" -> StringsEn
    else -> if (platformCode.lowercase().startsWith("en")) StringsEn else StringsDe
}

val LocalStrings = staticCompositionLocalOf { StringsDe }
